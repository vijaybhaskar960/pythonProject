'''
Explain the difference between a shallow copy and a deep copy in Python.

A shallow copy creates a new object that references the same memory location as the original object.
Any changes made to the copied object will also affect the original object.
In contrast, a deep copy creates a new object with its own memory location, and changes made to the copied object will not affect the original object.
'''


